public class OfficeExtraInfo {
    private int office_id;
    private String office_email;
    private String office_type_id;
    private String run_by_id;
    private String institute_name;

    public int getOffice_id() {
        return office_id;
    }

    public void setOffice_id(int office_id) {
        this.office_id = office_id;
    }

    public String getOffice_email() {
        return office_email;
    }

    public void setOffice_email(String office_email) {
        this.office_email = office_email;
    }

    public String getOffice_type_id() {
        return office_type_id;
    }

    public void setOffice_type_id(String office_type_id) {
        this.office_type_id = office_type_id;
    }

    public String getRun_by_id() {
        return run_by_id;
    }

    public void setRun_by_id(String run_by_id) {
        this.run_by_id = run_by_id;
    }

    public String getInstitute_name() {
        return institute_name;
    }

    public void setInstitute_name(String institute_name) {
        this.institute_name = institute_name;
    }
    
    
    
    
}
